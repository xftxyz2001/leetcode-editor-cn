class Solution {
    public int[] countNumbers(int cnt) {
        int c = (int) Math.pow(10, cnt);
        int[] ans = new int[c - 1];
        for (int i = 1; i < c; i++) {
            ans[i - 1] = i;
        }
        return ans;
    }
}
