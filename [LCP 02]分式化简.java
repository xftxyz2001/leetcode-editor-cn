class Solution {
    public int[] fraction(int[] cont) {
        int fenzi = cont[cont.length - 1];
        int fenmu = 1;
        for (int i = cont.length - 2; i >= 0; i--) {
            // å€’æ•°
            fenmu ^= fenzi;
            fenzi ^= fenmu;
            fenmu ^= fenzi;
            // åŠ ä¸Šå½“å‰çš„
            fenzi += cont[i] * fenmu;
        }
        return new int[]{fenzi, fenmu};
    }
}
